# გატესტეთ, გამოიყენეთ დღეს ნასწავლი ფუნქციები და კომენტარად მიუწერეთ ყველას, თუ რას აკეთებს

word = "gym"
print(word.find("y"))
# find() ფუნქცია მინიცჰებული მნიშვნელობის (სიმბოლოს) ინდექსს აბრუნებს

dd = "GYM"
print(dd.lower())
# lower() ფუნქციას სტრინგის თითოეული სიმბოლო lower case-ში გადააქ

idk = "power lifting"
print(idk.upper())
# upper() ფუნქციას სტრინგის ყველა სიმბოლო გადააქ upper case-ში

kk = "go to the gym"
print(kk.capitalize())
# capitalize() ფუნქციას სტრინგის ცალკეული სიტყვების პირველი სიმბოლოები აფერ ქეისში გადააქვს
